create table if not exists sys_user (
    user_id         bigint generated by default as identity primary key,
    username        varchar(64) not null,
    password_hash   varchar(255) not null,
    phone           varchar(32),
    email           varchar(128),
    display_name    varchar(64),
    user_type       varchar(16) not null default 'USER',
    status          varchar(16) not null default 'ACTIVE',
    deleted         tinyint not null default 0,
    created_at      timestamp not null default current_timestamp,
    updated_at      timestamp not null default current_timestamp
);

create table if not exists sys_verify_code_record (
    record_id       bigint generated by default as identity primary key,
    biz_type        varchar(64) not null,
    biz_id          varchar(64),
    subject_type    varchar(32),
    subject_id      bigint,
    target_type     varchar(16) not null,
    target_value    varchar(128) not null,
    channel         varchar(16) not null default 'SMS',
    template_id     varchar(100),
    provider        varchar(64),
    request_id      varchar(128),
    code_value      varchar(64) not null,
    expires_at      timestamp not null,
    used            boolean not null default false,
    used_at         timestamp,
    deleted         tinyint not null default 0,
    created_at      timestamp not null default current_timestamp,
    remark          varchar(500)
);

create table if not exists sys_auth_session (
    session_id      bigint generated by default as identity primary key,
    subject_id      bigint not null,
    subject_type    varchar(32) not null,
    login_type      varchar(32) not null,
    token_digest    varchar(64) not null,
    expires_at      timestamp not null,
    revoked         boolean not null default false,
    deleted         tinyint not null default 0,
    created_at      timestamp not null default current_timestamp,
    last_active_at  timestamp not null default current_timestamp,
    device_type     varchar(32),
    client_ip       varchar(64)
);
